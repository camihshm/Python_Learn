#----------------------------------------------------------------------------------
## Data Type: Complex Numbers.
## The data type complex numbers characteristics: 
## - Expressed like: ax + bj.
## - Has two elements 'real' and 'imaginary' part.
## - 'j' is the square root of -1.
#----------------------------------------------------------------------------------

#----------------------------------------------------------------------------------
# Example 1 - How attribute a complex number value for variables.
#----------------------------------------------------------------------------------

x = 2+3j
print('x', x)

y = 10+350j
print('y', y)

#----------------------------------------------------------------------------------
# Example 1 - Math operations with complex number values.
#----------------------------------------------------------------------------------

SUM = x + y
print('SUM', SUM)

SUB = x - y
print('SUB', SUB)

DIV = y/x
print('DIV', DIV)

MTL = y*x
print('MTL', MTL)